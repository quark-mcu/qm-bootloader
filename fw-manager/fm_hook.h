#ifndef __FM_HOOK_H__
#define __FM_HOOK_H__

/**
 * Firmware Management (FM) hook.
 *
 * Check if any FM condition is verified (FM pin or FM sticky bit) and, if so,
 * enter FM mode.
 *
 * \note If FM mode is entered, this function does not return.
 */
void fm_hook(void);

/**
 * Firmware Management secure entry.
 *
 * Set up the MPR and FPR policy and go into firmware management mode.
 *
 * \note This function does not return.
 */
void fm_secure_entry(void);

#endif /* __FM_HOOK_H__ */
